    private void bttrocarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttrocarActionPerformed
        if(temp.startsWith("-")){
            temp = temp.replace('-', '+');
        }else if(temp.startsWith("+")){
            temp = temp.replace('+', '-');
        }else{
            temp = "-" + temp;
        }
        txtpainel.setText(temp);
    }//GEN-LAST:event_bttrocarActionPerformed

