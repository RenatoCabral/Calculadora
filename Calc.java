    private void ResultadoOperacao(char op) throws NumberFormatException {
        operacao = op;
        num1 = Double.parseDouble(txtpainel.getText());
        txtpainel.setText("");
    }

    private void btigualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btigualActionPerformed
        num2 = Double.parseDouble(txtpainel.getText());
        
        switch(operacao){
            
            case '+':
                resultado = num1 + num2;
                break;
                
            case '-':
                resultado = num1 - num2;
                break;
                
            case '*':
                resultado = num1 * num2;
                break;
                
            case '/':
                resultado = num1 / num2;
                break;
        }
        txtpainel.setText(String.valueOf(resultado));
    }//GEN-LAST:event_btigualActionPerformed

